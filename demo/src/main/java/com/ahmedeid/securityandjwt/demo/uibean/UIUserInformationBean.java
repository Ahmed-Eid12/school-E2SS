package com.ahmedeid.securityandjwt.demo.uibean;

public class UIUserInformationBean {

	private int id;
	private long code;
	private String address;
	private int phone;
	private int preDegree;
	private String country;
	private String city;
	private String image;
	private int cardId_NO;
	private String qualification;
	private String className;
	private int experienceYear_NO;

	public UIUserInformationBean() {
		super();
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public long getCode() {
		return code;
	}

	public void setCode(long code) {
		this.code = code;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public int getPhone() {
		return phone;
	}

	public void setPhone(int phone) {
		this.phone = phone;
	}

	public int getPreDegree() {
		return preDegree;
	}

	public void setPreDegree(int preDegree) {
		this.preDegree = preDegree;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public int getCardId_NO() {
		return cardId_NO;
	}

	public void setCardId_NO(int cardId_NO) {
		this.cardId_NO = cardId_NO;
	}

	public String getQualification() {
		return qualification;
	}

	public void setQualification(String qualification) {
		this.qualification = qualification;
	}

	public String getClassName() {
		return className;
	}

	public void setClassName(String className) {
		this.className = className;
	}

	public int getExperienceYear_NO() {
		return experienceYear_NO;
	}

	public void setExperienceYear_NO(int experienceYear_NO) {
		this.experienceYear_NO = experienceYear_NO;
	}
}
